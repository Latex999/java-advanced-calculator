package com.calculator.model;

/**
 * Enum representing the different modes of the calculator.
 */
public enum CalculatorMode {
    /**
     * Standard mode with basic arithmetic operations.
     */
    STANDARD,
    
    /**
     * Scientific mode with advanced mathematical functions.
     */
    SCIENTIFIC,
    
    /**
     * Programmer mode with bitwise operations and number system conversions.
     */
    PROGRAMMER,
    
    /**
     * Conversion mode for unit conversions.
     */
    CONVERSION
}